class Arithmetic{
	public static void main(String args[]){
		int a=10;
		int b=20;
		int c,d,e,f,g;
		c=a+b;
		d=a-b;
		e=a*b;
		f=a/b;
		g=a%b;
		
		System.out.println("Sum of A and B is " + c);
		System.out.println("Sub of A and B is " + d);
		System.out.println("Mul of A and B is " + e);
		System.out.println("Div of A and B is " + f);
		System.out.println("Mod of A and B is " + g);
		
	}//main ends 
}//class ends 